public class average {
    public static void main ( String[]args )
    {
        int x = 12 ;
        int y = 14 ;
        int z = 8 ;
        System.out.println("Rainfall in April: " + x);
        System.out.println("Rainfall in May: " + y);
        System.out.println("Rainfall in June: " + z);
        System.out.println("The average is: " + ( ( x + y + z ) / 3.0) );


    }
}